package com.example.cuatroenraya;

public class Coordenada {
    private int fila;
    private int columna;

    public Coordenada(int fila,int columna){
        this.fila = fila;
        this.columna = columna;
    }

    public int getFila(){return this.fila;}
    public int getColumna(){return this.columna;}
}
